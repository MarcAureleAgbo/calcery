import fs from 'node:fs';
import path from 'node:path';
import { PATH_REDIRECTS } from '../redirects.config.mjs';

const outputPath = path.join(process.cwd(), 'public', '_redirects');
const sourcePaths = new Set(PATH_REDIRECTS.map(([source]) => source));

for (const [source, destination] of PATH_REDIRECTS) {
  if (!source.startsWith('/') || source === '/' || source.endsWith('/')) {
    throw new Error(`Redirect source must be a normalized non-root path: ${source}`);
  }
  if (!destination.startsWith('/') || !destination.endsWith('/')) {
    throw new Error(`Redirect destination must be a trailing-slash path: ${destination}`);
  }
  if (sourcePaths.has(destination.replace(/\/+$/, ''))) {
    throw new Error(`Redirect chain detected: ${source} -> ${destination}`);
  }
}

const lines = [
  '# Generated by scripts/generate-cloudflare-redirects.mjs.',
  '# Do not edit this file directly; update redirects.config.mjs.',
  '# Legacy path redirects. Both slash variants are explicit HTTP 301 rules.',
  ...PATH_REDIRECTS.flatMap(([source, destination]) => [
    `${source} ${destination} 301`,
    `${source}/ ${destination} 301`,
  ]),
  '',
];

fs.writeFileSync(outputPath, lines.join('\n'));
console.log(`Generated ${PATH_REDIRECTS.length * 2} static path redirects in public/_redirects.`);
